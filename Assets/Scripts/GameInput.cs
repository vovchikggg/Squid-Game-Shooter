using System;
using UnityEngine;
using UnityEngine.InputSystem;

public class GameInput : MonoBehaviour
{
    private PlayerInputActions playerInputActions;

    public static GameInput Instance { get; private set; }

    public event EventHandler OnPlayerAttack;

    private void Awake()
    {
        Instance = this;
        playerInputActions = new PlayerInputActions();
        playerInputActions.Enable();

        playerInputActions.Combat.Attack.started += PlayerAttack_started; //добавляем функцию в Actions
    }

    private void PlayerAttack_started(InputAction.CallbackContext obj)
    {
        if (OnPlayerAttack != null) //Есть объекты, которые вызывают
            OnPlayerAttack.Invoke(this,EventArgs.Empty);
    }

    public Vector2 GetMovementVector() // для работы с InputActions
    {
        var inputVector = playerInputActions.Player.Move.ReadValue<Vector2>();
        return inputVector;
    }

    public Vector3 GetMousePosition() //переделать метод добавив в класс GameInput
    {
        var mousePos = Input.mousePosition;
        return mousePos;
    }
}
